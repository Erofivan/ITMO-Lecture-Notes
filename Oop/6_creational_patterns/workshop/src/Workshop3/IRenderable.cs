// Базовый интерфейс для всех объектов, которые могут быть преобразованы в строковое представление
// 
// Роль в проекте:
// Это корневой интерфейс всей иерархии объектов. Он определяет единый контракт для всех элементов,
// которые должны уметь отображать себя в виде текста. Благодаря этому интерфейсу мы можем работать
// с различными типами объектов (текст, параграфы, статьи) через единый API.
//
// Применяемые принципы:
// - Программирование на основе интерфейсов (Interface Segregation Principle)
// - Полиморфизм - разные классы могут реализовывать этот интерфейс по-своему
namespace Workshop3;

public interface IRenderable
{
    // Преобразует объект в его строковое представление
    // 
    // Возвращает: строку, представляющую визуальное отображение объекта
    //
    // Этот метод является точкой входа для получения текстового представления любого объекта
    // в системе. Каждая реализация определяет свою собственную логику рендеринга.
    string Render();
}
