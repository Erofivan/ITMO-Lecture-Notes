// Интерфейс для параграфа - логической единицы текста с заголовком и содержимым
//
// Роль в проекте:
// Представляет собой маркерный интерфейс (marker interface) для параграфов.
// Наследуется от IRenderable, что означает, что любой параграф должен уметь
// преобразовываться в строковое представление.
//
// Зачем нужен отдельный интерфейс:
// Хотя IParagraph не добавляет новых методов к IRenderable, он служит для
// семантического разделения типов. Это позволяет:
// - Создавать методы, которые работают конкретно с параграфами
// - Различать параграфы и другие IRenderable объекты на уровне типов
// - Обеспечивать type-safety в методах типа IArticleBuilder.AddParagraph()
//
// Реализации:
// - DefaultParagraph - базовая реализация с заголовком, содержимым и футером
// - StyledParagraph - декоратор, добавляющий стилизацию к параграфу
//
// Связь с другими компонентами:
// - Используется в IArticleBuilder для типобезопасного добавления параграфов
// - Создается через паттерн Builder (IParagraphBuilder)
// - Может быть стилизован через паттерн Decorator (StyledParagraph)
namespace Workshop3.Paragraphs;

public interface IParagraph : IRenderable;
