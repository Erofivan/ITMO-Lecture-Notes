// Интерфейс фабрики для создания построителей параграфов
//
// Роль в проекте:
// Определяет контракт для создания объектов-построителей параграфов.
// Это ключевой элемент паттерна Abstract Factory, который позволяет
// создавать семейства связанных объектов без указания конкретных классов.
//
// Применяемый паттерн:
// Abstract Factory (Абстрактная фабрика) - для создания семейств связанных объектов
//
// Зачем нужна фабрика построителей:
// Прямое создание построителей (new DefaultParagraphBuilder()) привязывает код
// к конкретным реализациям. Фабрика же позволяет:
// 1. Легко переключаться между различными типами параграфов (обычные/стилизованные)
// 2. Централизованно управлять созданием построителей
// 3. Внедрять зависимости (например, модификаторы) в построители
//
// Пример применения в Program.cs:
//   IParagraphBuilderFactory factory = new StyledParagraphBuilderFactory(new ColorModifer(Color.Red));
//   // Все параграфы, созданные через эту фабрику, будут красными
//
// Связь с другими компонентами:
// - Реализуется в DefaultParagraphBuilderFactory и StyledParagraphBuilderFactory
// - Используется в BuildParagraphs() для создания набора параграфов
// - Позволяет легко менять стиль всех параграфов, заменив одну строку кода
namespace Workshop3.Paragraphs;

public interface IParagraphBuilderFactory
{
    // Создает новый экземпляр построителя параграфов
    //
    // Возвращает: построитель в начальном состоянии (IParagraphTitleSelector)
    //
    // Метод возвращает IParagraphTitleSelector, а не IParagraphBuilder, чтобы
    // обеспечить типобезопасное построение: первым шагом обязательно должна быть
    // установка заголовка через WithTitle().
    //
    // Каждый вызов CreateBuilder() должен возвращать новый независимый экземпляр,
    // чтобы параллельное построение нескольких параграфов не вызывало конфликтов.
    //
    // Реализации:
    // - DefaultParagraphBuilderFactory создает построители для обычных параграфов
    // - StyledParagraphBuilderFactory создает построители для стилизованных параграфов
    //   с заданным модификатором (цвет, жирность и т.д.)
    IParagraphTitleSelector CreateBuilder();
}
