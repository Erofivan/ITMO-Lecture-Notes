// Интерфейс модификатора для изменения отображаемого текста
//
// Роль в проекте:
// Определяет контракт для объектов, которые могут применять различные стилистические изменения
// к строковому представлению контента (например, добавление цвета, жирности, затемнения).
//
// Применяемый паттерн:
// Strategy (Стратегия) - каждая реализация этого интерфейса представляет собой отдельную стратегию
// модификации текста. Это позволяет легко добавлять новые способы стилизации без изменения
// существующего кода.
//
// Связь с другими компонентами:
// - Используется в классе Text для применения множественных модификаций к тексту
// - Используется в StyledParagraph для стилизации всего параграфа целиком
// - Конкретные реализации: BoldModifier, ColorModifier, DimModifier
namespace Workshop3.RenderableModifiers;

public interface IRenderableModifier
{
    // Применяет модификацию к переданному тексту
    //
    // Параметры:
    //   value - исходная строка, которую необходимо модифицировать
    //
    // Возвращает: модифицированную строку с примененным эффектом
    //
    // Примеры использования:
    // - BoldModifier применяет жирное начертание
    // - ColorModifier окрашивает текст в заданный цвет
    // - DimModifier делает текст приглушенным (тусклым)
    string Apply(string value);
}
