// Модификатор для создания приглушенного (тусклого) отображения текста
//
// Роль в проекте:
// Конкретная реализация стратегии модификации текста, которая делает текст менее ярким,
// приглушенным. Полезно для отображения второстепенной информации или подсказок.
//
// Применяемый паттерн:
// Strategy (Стратегия) - это одна из стратегий модификации текста
//
// Связь с другими компонентами:
// - Реализует интерфейс IRenderableModifier
// - Может использоваться совместно с другими модификаторами для создания комбинированных эффектов
// - Применяется через механизм цепочки модификаторов в Text.Render()
namespace Workshop3.RenderableModifiers;

public sealed class DimModifier : IRenderableModifier
{
    // Применяет эффект приглушения (затемнения) к переданному тексту
    //
    // Параметры:
    //   value - исходная строка текста
    //
    // Возвращает: строку с примененным эффектом приглушения (через ANSI escape-коды)
    //
    // Использует библиотеку Crayon для добавления ANSI escape-последовательностей,
    // которые делают текст менее контрастным. Это часто используется для отображения
    // вспомогательного текста, который не должен привлекать основное внимание пользователя.
    public string Apply(string value) 
        => Crayon.Output.Dim(value);
}
