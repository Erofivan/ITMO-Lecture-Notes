// Модификатор для применения жирного начертания к тексту
//
// Роль в проекте:
// Конкретная реализация стратегии модификации текста, которая делает текст жирным.
// Использует библиотеку Crayon для применения ANSI escape-последовательностей.
//
// Применяемый паттерн:
// Strategy (Стратегия) - это одна из стратегий модификации текста
//
// Связь с другими компонентами:
// - Реализует интерфейс IRenderableModifier
// - Используется в TextExtensions.Bold() для создания жирного текста
// - Может комбинироваться с другими модификаторами благодаря цепочке применения в Text.Render()
namespace Workshop3.RenderableModifiers;

public sealed class BoldModifier : IRenderableModifier
{
    // Применяет жирное начертание к переданному тексту
    //
    // Параметры:
    //   value - исходная строка текста
    //
    // Возвращает: строку с примененным жирным начертанием (через ANSI escape-коды)
    //
    // Использует библиотеку Crayon для кроссплатформенной работы с форматированием
    // текста в консоли. Метод Bold() добавляет специальные управляющие последовательности,
    // которые большинство современных терминалов интерпретируют как команду для
    // отображения текста жирным шрифтом.
    public string Apply(string value) 
        => Crayon.Output.Bold(value);
}
