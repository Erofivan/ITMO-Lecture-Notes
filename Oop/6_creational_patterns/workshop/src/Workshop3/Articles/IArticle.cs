// Интерфейс для статьи - составного документа из заголовка, параграфов и автора
//
// Роль в проекте:
// Представляет собой композитный интерфейс, объединяющий две роли:
// 1. IRenderable - статья может быть отрендерена в строку
// 2. IArticleBuilderDirector - статья может "обучить" построитель создать свою копию
//
// Применяемые паттерны:
// Composite (Компоновщик) - статья является составным объектом из параграфов
// Director (Директор) - статья может управлять процессом построения новой статьи
//
// Множественное наследование интерфейсов:
// IArticle наследует два интерфейса одновременно, что является стандартной практикой в C#.
// Это означает, что класс, реализующий IArticle, должен предоставить реализацию
// для методов из обоих интерфейсов: Render() и Direct().
//
// Зачем IArticleBuilderDirector в составе IArticle:
// Это интересное архитектурное решение - статья не только является данными,
// но и знает, как создать свою копию через построитель. Это позволяет:
// - Клонировать статьи с модификациями
// - Использовать существующую статью как шаблон для новой
// - Реализовать паттерн Prototype через механизм Builder
//
// Пример использования из Program.cs:
//   IArticle existingArticle = ...;
//   IArticleBuilder builder = new ArticleBuilder();
//   builder = existingArticle.Direct(builder); // статья "обучает" построитель
//   IArticle newArticle = builder.Build(); // создается копия статьи
//
// Связь с другими компонентами:
// - Реализуется классом Article
// - Создается через IArticleBuilder
// - Содержит параграфы (IParagraph)
// - Может управлять построителями (IArticleBuilder)
namespace Workshop3.Articles;

public interface IArticle : IRenderable, IArticleBuilderDirector;
