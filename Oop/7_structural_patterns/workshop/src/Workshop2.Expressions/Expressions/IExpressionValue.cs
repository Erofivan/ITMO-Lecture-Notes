// Файл определяет интерфейс для выражений, которые имеют конкретное числовое значение.
// Это специализация IExpression для случаев, когда выражение полностью вычислено
// и представляет собой константу.

namespace Workshop2.Expressions.Expressions;

// Интерфейс IExpressionValue представляет выражение с конкретным числовым значением.
// Наследуется от IExpression, добавляя свойство Value.
//
// Применение: используется для представления полностью вычисленных выражений,
// то есть констант. Когда выражение вычисляется полностью, результат
// всегда будет реализацией IExpressionValue (в данном случае ConstantExpression).
//
// Это позволяет отличить "готовое" числовое значение от выражения,
// которое ещё нужно вычислять или которое не может быть вычислено полностью.
public interface IExpressionValue : IExpression
{
    // Числовое значение выражения.
    // Это свойство доступно только для полностью вычисленных выражений.
    // Возвращает: числовое значение типа double
    double Value { get; }
}
