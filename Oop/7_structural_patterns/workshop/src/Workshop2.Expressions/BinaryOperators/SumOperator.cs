// Файл реализует оператор сложения как конкретную стратегию.
// Это простая реализация паттерна Strategy для операции сложения.

namespace Workshop2.Expressions.BinaryOperators;

// Класс SumOperator реализует операцию сложения.
// Паттерн Strategy: это конкретная стратегия для выполнения сложения.
// sealed означает, что класс нельзя наследовать — это законченная реализация.
public sealed class SumOperator : IBinaryOperator
{
    // Выполняет сложение двух чисел.
    // Параметры:
    //   left — первое слагаемое
    //   right — второе слагаемое
    // Возвращает: сумму двух чисел
    public double Apply(double left, double right)
        => left + right;

    // Возвращает символ операции для отображения в выражениях.
    // Возвращает: строку "+"
    public string Format()
        => "+";
}
