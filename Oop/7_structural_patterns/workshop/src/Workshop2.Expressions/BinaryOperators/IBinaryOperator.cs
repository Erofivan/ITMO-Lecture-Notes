// Файл определяет интерфейс для бинарных математических операторов (+, -, *, /).
// Это базовая абстракция, которая позволяет единообразно работать с различными операциями.
// Применяется паттерн Strategy — каждая операция инкапсулирована в отдельном классе,
// который реализует этот интерфейс.

namespace Workshop2.Expressions.BinaryOperators;

// Интерфейс IBinaryOperator представляет контракт для всех бинарных операций.
// Паттерн Strategy: позволяет выбирать алгоритм выполнения операции во время выполнения.
// Это даёт возможность легко добавлять новые операции без изменения существующего кода.
public interface IBinaryOperator
{
    // Применяет бинарную операцию к двум числам.
    // Параметры:
    //   left — левый операнд
    //   right — правый операнд
    // Возвращает: результат применения операции
    double Apply(double left, double right);

    // Возвращает строковое представление оператора для форматирования выражений.
    // Например: "+", "-", "*", "/"
    // Это нужно для красивого вывода выражений в текстовом виде.
    string Format();
}
