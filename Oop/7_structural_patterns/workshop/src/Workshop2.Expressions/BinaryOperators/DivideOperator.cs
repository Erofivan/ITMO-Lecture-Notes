// Файл реализует оператор деления как конкретную стратегию.
// Простая реализация операции деления без дополнительной логики.

namespace Workshop2.Expressions.BinaryOperators;

// Класс DivideOperator реализует операцию деления.
// Паттерн Strategy: конкретная стратегия для выполнения деления.
// sealed означает финальную реализацию без возможности наследования.
public sealed class DivideOperator : IBinaryOperator
{
    // Выполняет деление одного числа на другое.
    // Параметры:
    //   left — делимое
    //   right — делитель
    // Возвращает: частное left / right
    //
    // Примечание: в учебных целях не добавлена проверка деления на ноль,
    // чтобы сохранить код простым и сфокусироваться на демонстрации паттернов.
    public double Apply(double left, double right)
        => left / right;

    // Возвращает символ операции для отображения в выражениях.
    // Возвращает: строку "/"
    public string Format()
        => "/";
}
